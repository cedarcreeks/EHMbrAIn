def fig_stations(prob):
    labels = [f'{num}' for num, _, _ in STATIONS]
    pt = [float(prob.get_val(f'DESIGN.{el}:tot:P', units='bar')[0]) for _, el, _ in STATIONS]
    tt = [float(prob.get_val(f'DESIGN.{el}:tot:T', units='degK')[0]) for _, el, _ in STATIONS]
    fig, (ax1, ax2) = plt.subplots(2, 1, figsize=(4.8, 3.6), sharex=True)
    ax1.plot(labels, pt, 'o-', color=BLUE, linewidth=2, markersize=5)
    ax1.set_ylabel('$P_t$ [bar]')
    ax2.plot(labels, tt, 'o-', color=BLUE, linewidth=2, markersize=5)
    ax2.set_ylabel('$T_t$ [K]')
    ax2.set_xlabel('SAE ARP755A station')
    fig.align_ylabels()
    fig.tight_layout()
    fig.savefig(FIG_DIR / 'design_stations.pdf')
    plt.close(fig)
    return {num: {'Pt_bar': p, 'Tt_K': t} for (num, _, _), p, t in zip(STATIONS, pt, tt)}


