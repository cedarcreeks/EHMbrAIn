def main():
    FIG_DIR.mkdir(parents=True, exist_ok=True)
    TAB_DIR.mkdir(parents=True, exist_ok=True)
    targets = yaml.safe_load((REPO_ROOT / 'conf' / 'cfm56_7b_targets.yaml').read_text())

    print('Artifact-derived assets...')
    artifact_assets()

    print('Solving design point + anchors...')
    prob = build_problem(od=True)
    prob.set_solver_print(level=-1)
    converged = solve_anchors(prob)
    assert all(converged.values()), converged

    ds = design_summary(prob)
    anchors = {name: anchor_summary(prob, name) for name in MPCFM56.OD_ANCHORS}

    print('Sweeping the SLS working line...')
    sweep = sweep_working_line(prob)

    print('Generating figures and tables...')
    fig_anchor_errors(anchors, targets)
    fig_working_line(sweep, targets)
    stations = fig_stations(prob)
    tabla_anclas(anchors, targets)
    tabla_diseno(ds)
    tabla_estaciones(stations)

    (FIG_DIR / 'assets.json').write_text(json.dumps({
        'generated': date.today().isoformat(),
        'design_point': ds, 'anchors': anchors, 'sls_sweep': sweep,
        'stations': stations,
    }, indent=2))
    print(f'OK -> {FIG_DIR} y {TAB_DIR}')


if __name__ == '__main__':
    main()
