def sweep_working_line(prob):
    """Walk the A5 point across the SLS power range, collecting (PC, Fn, WF, N1, N2, OPR)."""
    pts = []
    for pc in (0.07, 0.10, 0.15, 0.22, 0.30, 0.40, 0.55, 0.70, 0.85, 1.00):
        prob.set_val('A5_idle.PC', pc)
        prob.run_model()
        if not anchor_converged(prob, 'A5_idle', pc):
            continue
        s = anchor_summary(prob, 'A5_idle')
        pts.append({'PC': pc, **s})
    prob.set_val('A5_idle.PC', MPCFM56.OD_ANCHORS['A5_idle'])
    return pts


def fig_anchor_errors(anchors, targets):
    fig, ax = plt.subplots(figsize=(4.8, 2.6))
    names, errs, tols, gated = [], [], [], []
    for name, s in anchors.items():
        t = targets['anchors_offdesign'][name]
        names.append(name.split('_')[0])
        errs.append((s['WF_kgps'] - t['wf_kgps']['value']) / t['wf_kgps']['value'] * 100)
        tols.append(t['wf_kgps']['tol_pct'])
        gated.append(t.get('gated', True))
    x = range(len(names))
    for i, tol in enumerate(tols):
        ax.fill_between([i - 0.3, i + 0.3], -tol, tol, color=BAND, alpha=0.55, linewidth=0)
    ax.axhline(0, color=GRAY, linewidth=0.8)
    for i, (e, g) in enumerate(zip(errs, gated)):
        ax.plot(i, e, 'o', markersize=7, color=BLUE if g else 'white',
                markeredgecolor=BLUE, markeredgewidth=1.4, zorder=3)
        ax.annotate(f'{e:+.1f} %', (i, e), textcoords='offset points',
                    xytext=(10, 2), fontsize=8)
    ax.set_xticks(list(x), ['Takeoff\n(100 %)', 'Climb-out\n(85 %)',
                            'Approach\n(30 %)', 'Idle\n(7 %)'])
    ax.set_ylabel('Fuel-flow error vs. EEDB [%]')
    ax.set_ylim(-8, 13)
    fig.tight_layout()
    fig.savefig(FIG_DIR / 'anchor_errors.pdf')
    plt.close(fig)


def fig_working_line(sweep, targets):
    fig, ax = plt.subplots(figsize=(4.8, 3.0))
    fn = [p['Fn_lbf'] * 4.448222 / 1000 for p in sweep]   # kN
    wf = [p['WF_kgps'] for p in sweep]
    ax.plot(fn, wf, '-', color=BLUE, linewidth=2, label='Model (SLS working line)')
    eedb_fn, eedb_wf = [], []
    for name, pc in MPCFM56.OD_ANCHORS.items():
        t = targets['anchors_offdesign'][name]
        eedb_fn.append(pc * 116.99)
        eedb_wf.append(t['wf_kgps']['value'])
